#include <stdio.h>

#define N_MAX 26
#define R_MAX 26

char nArray[N_MAX] = { 0, };
char rArray[R_MAX] = { 0, };

void SetupInput(int n_size);
void Combination(int n, int r, int sizeOfPickUp);
void printf_array_r(int sizeOfPickUp);

int main(int argc, char argv[]) {

	int T;
	int n, r;

	scanf("%d", &T);

	for (int test_case = 1; test_case <= T; test_case++) {
		scanf("%d %d ", &n, &r);

		SetupInput(n);

		printf("#%d\n", test_case);
		Combination(n, r, r);
	}

	return 0;
}

void SetupInput(int n_size) {
	int i = 0;
	while (i < n_size) {
		nArray[i] = getchar();
		i++;
	}
}


void Combination(int n, int r, int sizeOfPickUp) {
	if (r == 0) {
		printf_array_r(sizeOfPickUp);
	}
	else if (n < r) {
		return;
	}
	else {
		rArray[r - 1] = nArray[n - 1];
		Combination(n - 1, r - 1, sizeOfPickUp);
		Combination(n - 1, r, sizeOfPickUp);
	}
}

void printf_array_r(int sizeOfPickUp) {
	int i = 0;

	while (i < sizeOfPickUp) {
		printf("%c", rArray[i]);
		i++;
	}

	putchar('\n');
}

